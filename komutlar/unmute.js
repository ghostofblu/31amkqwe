const Discord = require("discord.js");
const frapuhin = require('../frapuhin.json')
const db = require("quick.db")
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
exports.run = async (client, message, args, bot, params, perms, guild) => {
if(!message.member.roles.get(frapuhin.mutehammer) && !message.member.hasPermission('ADMINISTRATOR') && !message.member.roles.get(frapuhin.ownerrolu)) return message.channel.send(`Bu komutu kullanmaya erişimin yok`).then(msg => msg.delete(4000))
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
var renkler = [0x350349, 0xfb1a1e, 0x220d02, 0x7554e7, 0xdb8f7c];
var randomrenk = Math.floor(Math.random() * renkler.length);
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
let member = message.guild.member(message.mentions.users.first() || message.guild.members.get(args[0]));
if(!member) return message.channel.send(`Bir kullanıcı etiketlemeli veya ID yazmalısın`).then(msg => msg.delete(6000))
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
setTimeout(function() {member.removeRole(frapuhin.chatmute)}, 1000);
member.setMute(false)
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
const aresMessage = new Discord.RichEmbed()
.setDescription(`${member} Adlı üyenin mutesi ses ve yazı kanallarında açılmıştır <${frapuhin.tikemoji2}>`)
.setColor(renkler[randomrenk])
message.channel.send(aresMessage)};
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
exports.conf = {
  enabled: true,
  guildOnly: false,
  aliases: ['unmute', 'sesunmute', 'chatunmute', 'voiceunmute', 'yazıunmute', 'yaziunmute']
};

exports.help = {
  name: 'unmute',
  description: 'Belirttiğiniz kullanıcıyı süreli susturur.',
  usage: ' [Kullanıcı] [Süre]'
};